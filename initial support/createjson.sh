#!/bin/bash

maintainer="GuidixX (GuidixX)"                                                        # Here we get the name of maintainer
path=~/cr                                                                             # Here you will need to specify the path to the crDroid source code folder
device=spes                                                       # Here we get the name of the device based on the name of the folder
zip=$(basename $path/out/target/product/spes/crDroidAndroid-11.0-*-spes-v7.39.zip)        # Here we get the package name with the extension .zip
date=$(echo $zip | cut -f3 -d '-')                                                          # Here we get the build date (in YYYY-MM-DD format)

  case "${device,,}" in 

        "spes"         ) devicename="Redmi Note 11" && oem="Xiaomi"       ;;  #

        esac

buildtype="Monthly"                          # choose from Testing/Alpha/Beta/Weekly/Monthly
forum=""   # https link (mandatory)
gapps="https://sourceforge.net/projects/nikgapps/files/Releases/NikGapps-R/07-Mar-2024/NikGapps-core-arm64-11-20240307-signed.zip/download" #https link (leave empty if unused)
firmware=""                                  # https link (leave empty if unused)
modem=""                                     # https link (leave empty if unused)
bootloader=""                                # https link (leave empty if unused)
recovery=""                                  # https link (leave empty if unused)
paypal="https://paypal.me/GuidixMusic"       # https link (leave empty if unused)
telegram="https://t.me/DragonTurbo"              # https link (leave empty if unused)

#don't modify from here
zip_name=$path/out/target/product/spes/$zip
buildprop=$path/out/target/product/spes/system/build.prop

linenr=`grep -n "ro.system.build.date.utc" $buildprop | cut -d':' -f1`
timestamp=`sed -n $linenr'p' < $buildprop | cut -d'=' -f2`
zip_only=`basename "$zip_name"`
md5=`md5sum "$zip_name" | cut -d' ' -f1`
sha256=`sha256sum "$zip_name" | cut -d' ' -f1`
size=`stat -c "%s" "$zip_name"`
version=`echo "$zip_only" | cut -d'-' -f5`
v_max=`echo "$version" | cut -d'.' -f1 | cut -d'v' -f2`
v_min=`echo "$version" | cut -d'.' -f2`
version=`echo $v_max.$v_min`

echo '{
  "response": [
    {
        "maintainer": "'$maintainer'",
        "oem": "'$oem'",
        "device": "'$devicename'",
        "filename": "'$zip_only'",
        "download": "https://sourceforge.net/projects/guidixproject/crdroid/stable/'$zip'/download",
        "timestamp": '$timestamp',
        "md5": "'$md5'",
        "sha256": "'$sha256'",
        "size": '$size',
        "version": "'$version'",
        "buildtype": "'$buildtype'",
        "forum": "'$forum'",
        "gapps": "'$gapps'",
        "firmware": "'$firmware'",
        "modem": "'$modem'",
        "bootloader": "'$bootloader'",
        "recovery": "'$recovery'",
        "paypal": "'$paypal'",
        "telegram": "'$telegram'",
    }
  ]
}' >/$device.json

git add -A && git commit -m "Update autogenerated json for spes" && git push
gh release create $zip --notes "Automated release crDroid for Spes" ~/cr/out/target/product/spes/$zip